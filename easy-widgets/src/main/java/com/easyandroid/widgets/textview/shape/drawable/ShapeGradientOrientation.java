package com.easyandroid.widgets.textview.shape.drawable;

/**
 * package: com.easyandroid.widgets.textview.shape.drawable.ShapeGradientOrientation
 * author: gyc
 * description: Shape 渐变方向
 * time: create at 2022/6/29 10:59
 */
public enum ShapeGradientOrientation {

    /**
     * 从右上角到左下角绘制渐变
     */
    TR_BL,

    /**
     * 从右到左绘制渐变
     */
    RIGHT_LEFT,

    /**
     * 从右下角到左上角绘制渐变
     */
    BR_TL,

    /**
     * 从下到上绘制渐变
     */
    BOTTOM_TOP,

    /**
     * 从左下角到右上角绘制渐变
     */
    BL_TR,

    /**
     * 从左到右绘制渐变
     */
    LEFT_RIGHT,

    /**
     * 从左上角到右下角绘制渐变
     */
    TL_BR,

    /**
     * 从上到下绘制渐变
     */
    TOP_BOTTOM
}
